Num1 = input("Enter a number:")
Symbol = input("Enter a symbol:")
Num2 = input("Enter another Number:")

if Symbol == "+":
    print(float(Num1) + float(Num2) )
elif Symbol == "*":
    print(float(Num1) * float(Num2) )
elif Symbol == "-":
    print(float(Num1) - float(Num2) )
elif Symbol == "/":
    print(float(Num1) / float(Num2) )